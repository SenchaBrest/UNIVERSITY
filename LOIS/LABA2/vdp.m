function vdp()
    % Определяем начальные условия
    y0 = [2; 0];
    K = 1000;

    % Решаем уравнение
    [t, Y] = ode15s(@(t,y) vanderpoldyn(t,y,K), [0, 3000], y0);

    % Визуализируем решение
    plot(t, Y(:,1));
    xlabel('t');
    ylabel('y1');
    title('Решение уравнения Ван-дер-Поля');
end