def convert(number, base_from, base_to):
    number_in_dec = 0
    L = len(number)
    res = ''
    alphabet = "0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ"
    for n, i in zip(number, range(L)):
        n = alphabet.index(n)
        number_in_dec += n * base_from ** (L - i - 1)
    while number_in_dec > 0:
        number_in_dec, m = divmod(number_in_dec, base_to)
        res += alphabet[m]
    return res[::-1]
